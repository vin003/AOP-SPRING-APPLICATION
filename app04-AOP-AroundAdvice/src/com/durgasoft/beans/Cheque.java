package com.durgasoft.beans;

public class Cheque {
			private String cheque_No ; 
			private int  amount ;
			public String getCheque_No() {
				return cheque_No;
			}
			public void setCheque_No(String cheque_No) {
				this.cheque_No = cheque_No;
			}
			public int getAmount() {
				return amount;
			}
			public void setAmount(int amount) {
				this.amount = amount;
			} 
			
}
